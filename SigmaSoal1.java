public class SigmaSoal1 {
    public static void main(String[] args) {
        int sum = 0; // Inisialisasi hasil sigma
        for (int i = 0; i <= 3; i++) { // Loop dari i = 0 hingga i = 3
            sum += i; // Tambahkan nilai i ke hasil
        }
        System.out.println("Hasil Sigma Soal 1: " + sum); // Cetak hasil
    }
}
